#include "cub3D.h"

void    draw_background(t_cube *s)
{
	int	x;	
	int	y;	

	y = 0;
	while (y < s->map_h)
	{
		x = 0;
		while (x < s->map_l)
		{
			x++;
		}
		y++;
	}
}
